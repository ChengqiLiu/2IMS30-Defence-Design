#include  <cpu/include/cpu.h>
#include  <common/include/rtos_err.h>

#include  <canopen/include/canopen_types.h>
#include  <canopen/include/canopen_obj.h>

/********************************************************************************************************
 ********************************************************************************************************
 *                                              DATA TYPES
 ********************************************************************************************************
 *******************************************************************************************************/

/****************************************************************************************************//**
 *                                          OBJECT DICTIONARY
 *
 * @note     This data structure holds all information, which represents the object dictionary.
 *******************************************************************************************************/

typedef struct canopen_dict {
  CANOPEN_OBJ *RootPtr;                                         ///< Pointer to root object of dictionary
  CANOPEN_OBJ *LastObjUsedPtr;                                  ///< Pointer to the last object used by the node.
  CPU_INT16U  ObjQty;                                           ///< Current number of objects in dictionary.
} CANOPEN_DICT;

/********************************************************************************************************
 ********************************************************************************************************
 *                                         FUNCTION PROTOTYPES
 ********************************************************************************************************
 *******************************************************************************************************/

CPU_INT16S CANopen_DictInit(CANOPEN_DICT *p_dict,
                            CANOPEN_NODE *p_node,
                            CANOPEN_OBJ  *p_root_obj,
                            RTOS_ERR     *p_err);

CANOPEN_OBJ *CANopen_DictFind(CANOPEN_DICT *p_dict,
                              CPU_INT32U   key,
                              RTOS_ERR     *p_err);

